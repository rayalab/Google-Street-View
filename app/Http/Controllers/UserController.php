    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {

        $req = $request->all();

        $User = DB::table('users')
            ->where('user_id', $id)
            ->update([
                'run' => $req["run"],
                'cellphone' => $req["cellphone"],
                'address' => $req["address"],
                'email' => $req["email"]
         ]);


        return response()->json($User);
    }

    /**
